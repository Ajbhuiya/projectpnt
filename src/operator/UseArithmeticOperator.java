package operator;

public class UseArithmeticOperator {
    //use art operator
    //addition:+
    //substraction:-
    //Multiply= *
    //division=/
    //remainder= %
    public static void main(String[] args) {

        int number1=50;
        int number2=60;
        int summation=number1+number2;
        System.out.println(summation);


        int age = 20;
        int ag2 = 50;
        int age3 = 100;
        int sum=age-ag2-age3;
        System.out.println(sum);

        int n=20;
        int n2=40;
        int n4=40;
        int multiplication=n*n2*n4;
        System.out.println(multiplication);

        int a=20;
        int b=20;
        System.out.println(a-b);

    }
}